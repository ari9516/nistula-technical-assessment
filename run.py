#!/usr/bin/env python
"""
Run script for Nistula Guest Message Handler
Usage: python run.py
"""

import uvicorn

if __name__ == "__main__":
    uvicorn.run(
        "src.main:app",
        host="0.0.0.0",
        port=8000,
        reload=True,  # Auto-reload on code changes (development only)
        log_level="info",
    )